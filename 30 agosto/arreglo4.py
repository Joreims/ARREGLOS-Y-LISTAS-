#Leer una frase y convertirla a Mayúscula
frase = input("Dime una frase: ")
print("La frase en mayúscula es: ", frase.upper())
print("La frase en mayúscula es: ", frase.lower())
print("La frase con la primera letra en mayúscula es: ", frase.capitalize())
print("Titulo: ", frase.title())
print("La frase: ", frase.upper())
